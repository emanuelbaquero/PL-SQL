CREATE OR REPLACE PROCEDURE LANZAR_COMANDO(P_COMANDO VARCHAR(100))  
	AUTHID CURRENT_USER
	IS 
	BEGIN 
		EXECUTE IMMEDIATE COMANDO; 
	EXCEPTION WHEN OTHERS THEN RAISE_APLICATION_ERROR(-20000,'ERROR EN LA EJECUCION DEL COMANDO' || SQLERRM)
	END;
/
BEGIN
	LANZAR_COMANDO(CREATE TABLE INMMEDIATE_TABLA (V1 NUMBER, V2 VARCHAR2(100)));
END;
/
CREATE OR REPLACE PROCEDURE CREAR_SYNONYM(NOMBRE_TABLA VARCHAR2, NOMBRE_VIEW VARCHAR2) 
	AUTHID CURRENT_USER 
	IS 
	COMANDO VARCHAR2(100);
	BEGIN
		COMANDO := 'CREATE SYNONYM '||NOMBRE_VIEW ||' FOR '|| NOMBRE_TABLA;
		EXECUTE IMMEDIATE COMANDO;
	EXCETION WHEN OTHERS THEN RAISE_APPLICATION_ERROR(-20000,'ERROR EN LA EJECUCION DEL COMANDO' || SQLERRM);
	END;
/
	BEGIN
		CREAR_SYNONYM('SYNONYM_IMMEDIATE_TABLA2','IMMEDIATE_TABLA2');
	END;