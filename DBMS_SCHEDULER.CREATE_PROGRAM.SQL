SELECT * FROM ALL_OBJECTS WHERE OBJECT_NAME LIKE 'PROGRAMA' OR OBJECT_NAME LIKE 'JOB_PROGRAMA' 
/
BEGIN
    DBMS_SCHEDULER.ENABLE('PROGRAMA');
END;
/
BEGIN
    DBMS_SCHEDULER.CREATE_PROGRAM(
        PROGRAM_NAME => 'PROGRAMA',
        PROGRAM_ACTION => 'IMPRIMIR_NOMBRE.IMPRIMIR',
        PROGRAM_TYPE => 'EXECUTABLE',
        COMMENTS => 'MI PRIMER PROGRAM'
    );
END;

/
BEGIN
    DBMS_SCHEDULER.CREATE_JOB(
        JOB_NAME => 'JOB_PROGRAM',
        JOB_TYPE => 'PLSQL BLOCK',
        JOB_ACTION => 'PROGRAMA',
        START_DATE => '16-OCT-19 07.00.00 PM',
        REPEAT_INTERVAL => 'FREQ=SECONDLY;INTERVAL=10',
        END_DATE => '17-OCT-19 07.00.00 PM',
        AUTO_DROP => FALSE,
        COMMENTS => 'MI NUEVA ACCION');
END;
/
CREATE OR REPLACE PACKAGE IMPRIMIR_NOMBRE IS 
    PROCEDURE IMPRIMIR;
END IMPRIMIR_NOMBRE;
/
CREATE OR REPLACE PACKAGE BODY IMPRIMIR_NOMBRE IS 
    PROCEDURE IMPRIMIR IS 
        BEGIN    
            DBMS_OUTPUT.PUT_LINE('EMA');
        END;
END IMPRIMIR_NOMBRE;
/
SET SERVEROUTPUT ON
EXEC IMPRIMIR_NOMBRE.IMPRIMIR;