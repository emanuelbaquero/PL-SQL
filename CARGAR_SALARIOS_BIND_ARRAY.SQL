CREATE TABLE SALARIOS_EMPLE (
    COD_EMPLEADO NUMBER,
    SALARIO_BRUTO NUMBER,
    IMPUESTOS NUMBER,
    SALARIO_NETO NUMBER
);

CREATE OR REPLACE PROCEDURE CARGAR_SALARIOS(IMPUESTO NUMBER)
IS 
    EMPLEADOS DBMS_SQL.NUMBER_TABLE;
    SALARIOS DBMS_SQL.NUMBER_TABLE;
    IMPUESTOS DBMS_SQL.NUMBER_TABLE;
    NETO DBMS_SQL.NUMBER_TABLE;
    ID_CURSOR INTEGER;
    NUM_FILAS INTEGER;
BEGIN
    SELECT EMPLOYEE_ID, SALARY, SALARY*IMPUESTO/100, SALARY-(SALARY*IMPUESTO/100)
        BULK COLLECT INTO EMPLEADOS, SALARIOS,IMPUESTOS,NETO FROM HR.EMPLOYEES;

    ID_CURSOR := DBMS_SQL.OPEN_CURSOR;
    DBMS_SQL.PARSE(ID_CURSOR, 'INSERT INTO SALARIOS_EMPLE VALUES (:EMPLEADOS,:SALARIOS,:IMPUESTOS,:NETO)',DBMS_SQL.NATIVE);
    DBMS_SQL.BIND_ARRAY(ID_CURSOR,':EMPLEADOS',EMPLEADOS);
    DBMS_SQL.BIND_ARRAY(ID_CURSOR,':SALARIOS',SALARIOS);
    DBMS_SQL.BIND_ARRAY(ID_CURSOR,':IMPUESTOS',IMPUESTOS);
    DBMS_SQL.BIND_ARRAY(ID_CURSOR,':NETO',NETO);
    NUM_FILAS:= DBMS_SQL.EXECUTE(ID_CURSOR);
    DBMS_SQL.CLOSE_CURSOR(ID_CURSOR);
END;
/
EXECUTE CARGAR_SALARIOS(90);
/
SELECT * FROM SALARIOS_EMPLE 